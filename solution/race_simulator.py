def simulate_race(race_data):
    config = race_data['race_config']
    track = config['track']; laps = config['total_laps']; base = config['base_lap_time']; pit = config['pit_lane_time']; temp = config['track_temp']
    c = TRACK_CONSTANTS.get(track, (20,30,40,0.3,0,5,1,1,1,0.1,0.1,0.1))
    gs, gm, gh, kg, os, oh, ds, dm, dh, tcs, tcm, tch = c
    graces = {'SOFT': max(0, gs - kg*temp), 'MEDIUM': max(0, gm - kg*temp), 'HARD': max(0, gh - kg*temp)}
    offsets = {'SOFT': os, 'MEDIUM': 0.0, 'HARD': oh}
    degs = {'SOFT': ds, 'MEDIUM': dm, 'HARD': dh}; tms = {'SOFT': 1.0 + tcs*temp, 'MEDIUM': 1.0 + tcm*temp, 'HARD': 1.0 + tch*temp}
    
    times = {}
    for pk, st in race_data['strategies'].items():
        sid = st['driver_id']; start = int(pk.replace('pos', '')); cur = st['starting_tire']
        pits_laps = {p['lap']: p['to_tire'] for p in st['pit_stops']}
        total = 0.0; age = 0; pits_count = 0
        for l in range(1, laps+1):
            age += 1
            total += round(base + offsets[cur] + max(0, age - graces[cur]) * degs[cur] * tms[cur], 3)
            if l in pits_laps: cur = pits_laps[l]; age = 0; pits_count += 1
        times[sid] = (round(total + pits_count*pit, 3), start)
    
    return {'race_id': race_data['race_id'], 'finishing_positions': sorted(times.keys(), key=lambda x: times[x])}

